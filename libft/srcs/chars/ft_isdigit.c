int		ft_isdigit(int c)
{
	return ((c >= '0' && c <= '9') ? 1 : 0);
}

int		str_isdigit(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (!ft_isdigit(s[i]))
			return (0);
		i++;
	}
	return (1);
}
